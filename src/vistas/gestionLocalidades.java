    public void estadoInicial() {
        btn_nuevoGL.setEnabled(isEnabled());
        btn_editarGL.setEnabled(isEnabled());
        btn_cancelarGL.setEnabled(!isEnabled());
        btn_guardarGL.setEnabled(!isEnabled());
        btn_salirGL.setEnabled(isEnabled());
        
        txt_codigoGL.setEnabled(!isEnabled());
        txt_nombreGL.setEnabled(!isEnabled());
        
        cmb_provinciaGL.setEnabled(!isEnabled());
        
        cmb_buscarLocalidadesPorProvinciasGL.setEnabled(isEnabled());
        
        tb_listadoLocalidadGL.setEnabled(isEnabled());
        UIManager.put("ComboBox.disabledBackground", new Color(212, 212, 210));
        UIManager.put("ComboBox.disabledForeground", Color.BLACK);
        
        txt_codigoGL.setText("");
        txt_nombreGL.setText("");
        
        txt_codigoGL.setBackground(Color.white);
        txt_nombreGL.setBackground(Color.white);
        cmb_provinciaGL.setBackground(Color.WHITE);
        
        txt_codigoGL.setDisabledTextColor(Color.BLACK);
        txt_nombreGL.setDisabledTextColor(Color.BLACK);
        
        cargarTabla(this.cp.getControladoraCliente().listarLocalidades());
        Utilidades.cargarCombo(this.cp.getControladoraCliente().listarProvincias(), cmb_provinciaGL);
        Utilidades.cargarCombo(this.cp.getControladoraCliente().listarProvincias(), cmb_buscarLocalidadesPorProvinciasGL);
        
    }
    
    public void cambiarEstado() {
        
        Utilidades.cargarCombo(this.cp.getControladoraCliente().listarProvincias(), cmb_provinciaGL);
        Utilidades.cargarCombo(this.cp.getControladoraCliente().listarProvincias(), cmb_buscarLocalidadesPorProvinciasGL);
        
        tb_listadoLocalidadGL.setEnabled(!isEnabled());
        btn_nuevoGL.setEnabled(!isEnabled());
        btn_editarGL.setEnabled(!isEnabled());
        btn_cancelarGL.setEnabled(isEnabled());
        btn_guardarGL.setEnabled(isEnabled());
        txt_nombreGL.setEnabled(isEnabled());
        cmb_provinciaGL.setEnabled(isEnabled());
        
    }
    
    public void cargarTabla(List<Localidad> localidades) {
        DefaultTableModel tbCarga = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        if (!localidades.isEmpty()) {
            String Cabecera[] = {"Codigo", "Nombre", "Provincia"};
            tbCarga.setColumnIdentifiers(Cabecera);
            Object fila[] = new Object[tbCarga.getColumnCount()];
            tb_listadoLocalidadGL.getTableHeader().setFont(new Font("Century Ghotic", Font.BOLD, 14));
            for (Localidad l : localidades) {
                fila[0] = l.getCodigo();
                fila[1] = l.getNombre();
                fila[2] = l.getProvincia();
                tbCarga.addRow(fila);
            }
            
        }
        tb_listadoLocalidadGL.setModel(tbCarga);
    }

