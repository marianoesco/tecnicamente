    private void btn_guardarGLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarGLActionPerformed
        
        try {
            if (!txt_nombreGL.getText().trim().isEmpty() && cmb_provinciaGL.getSelectedItem() != "[SELECCIONAR]" && cmb_provinciaGL.getSelectedItem() != null) {
                String nombre = txt_nombreGL.getText().trim();
                Provincia provincia = (Provincia) cmb_provinciaGL.getSelectedItem();
                if (txt_codigoGL.getText().trim().isEmpty()) {
                    
                    Localidad localidad = new Localidad(nombre, provincia);
                    this.cp.getControladoraCliente().registrarLocalidad(localidad);
                    JOptionPane.showMessageDialog(null, "Creado con Exito", "Gestion de Localidades", JOptionPane.INFORMATION_MESSAGE);
                    
                } else {
                    Integer codigo = Integer.valueOf(txt_codigoGL.getText());
                    
                    Localidad localidad = this.cp.getControladoraCliente().buscarLocalidadPorCodigo(codigo);
                    localidad.setNombre(nombre);
                    localidad.setProvincia(provincia);
                    
                    this.cp.getControladoraCliente().modificarLocalidad(localidad);
                    
                    JOptionPane.showMessageDialog(null, "Editado con Exito", "Gestion de Localidades", JOptionPane.INFORMATION_MESSAGE);
                    
                }
                this.estadoInicial();
            } else {
                
                throw new Exception("Todos los campos son obligatorios");
                
            }
            
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Gestion de Localidades", JOptionPane.WARNING_MESSAGE);
            
        }
        

    }//GEN-LAST:event_btn_guardarGLActionPerformed

