    private void cmb_buscarLocalidadesPorProvinciasGLItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_buscarLocalidadesPorProvinciasGLItemStateChanged
        if (cmb_buscarLocalidadesPorProvinciasGL.getSelectedItem() != "[SELECCIONAR]" && cmb_buscarLocalidadesPorProvinciasGL.getSelectedItem() != null) {
            Provincia provincia = (Provincia) cmb_buscarLocalidadesPorProvinciasGL.getSelectedItem();
            cargarTabla(this.cp.getControladoraCliente().filtrarLocalidadPorProvincia(provincia));
        } else {
            cargarTabla(this.cp.getControladoraCliente().listarLocalidades());
        }
        

    }//GEN-LAST:event_cmb_buscarLocalidadesPorProvinciasGLItemStateChanged

