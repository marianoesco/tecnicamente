    private void btn_editarGLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarGLActionPerformed
        if (tb_listadoLocalidadGL.getSelectedRow() > -1 && tb_listadoLocalidadGL.getSelectedRowCount() == 1) {
            Integer seleccionado = (Integer) tb_listadoLocalidadGL.getValueAt(tb_listadoLocalidadGL.getSelectedRow(), 0);
            Localidad localidadSeleccionado = this.cp.getControladoraCliente().buscarLocalidadPorCodigo(seleccionado);
            
            if (localidadSeleccionado != null) {
                this.cambiarEstado();
                
                txt_codigoGL.setText(String.valueOf(localidadSeleccionado.getCodigo()));
                txt_nombreGL.setText(localidadSeleccionado.getNombre());
                cmb_provinciaGL.setSelectedItem(localidadSeleccionado.getProvincia());
                
            }
            
            tb_listadoLocalidadGL.clearSelection();
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un localidad", "Gestion de Localidades", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btn_editarGLActionPerformed

