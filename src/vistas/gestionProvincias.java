    private void txt_buscarPorProvinciaGPKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_buscarPorProvinciaGPKeyReleased
        if (!txt_buscarPorProvinciaGP.getText().trim().isEmpty()) {
            String nombre = txt_buscarPorProvinciaGP.getText().trim();
            cargarTabla(this.cp.getControladoraCliente().filtrarProvinciaPorNombre(nombre));
        } else {
            cargarTabla(this.cp.getControladoraCliente().listarProvincias());
        }
    }//GEN-LAST:event_txt_buscarPorProvinciaGPKeyReleased

    public void cargarTabla(List<Provincia> provincias) {
        DefaultTableModel tbCarga = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        if (!provincias.isEmpty()) {
            String Cabecera[] = {"Codigo", "Nombre"};
            tbCarga.setColumnIdentifiers(Cabecera);
            Object fila[] = new Object[tbCarga.getColumnCount()];
            tb_listadoProvinciasGP.getTableHeader().setFont(new Font("Century Ghotic", Font.BOLD, 14));
            for (Provincia p : provincias) {
                fila[0] = p.getCodigo();
                fila[1] = p.getNombre();
                tbCarga.addRow(fila);
            }
           
        }
         tb_listadoProvinciasGP.setModel(tbCarga);
    }

    public void estadoInicial() {
        btn_nuevoGP.setEnabled(isEnabled());
        btn_editarGP.setEnabled(isEnabled());
        btn_cancelarGP.setEnabled(!isEnabled());
        btn_guardarGP.setEnabled(!isEnabled());
        btn_salirGP.setEnabled(isEnabled());

        txt_codigoGP.setEnabled(!isEnabled());
        txt_codigoGP.setDisabledTextColor(Color.BLACK);
        txt_nombreGP.setDisabledTextColor(Color.BLACK);
        
        txt_buscarPorProvinciaGP.setEnabled(isEnabled());
        txt_buscarPorProvinciaGP.setText("");
        
        tb_listadoProvinciasGP.setEnabled(isEnabled());

        txt_codigoGP.setText("");
        txt_nombreGP.setText("");

        txt_nombreGP.setEnabled(!isEnabled());

        txt_nombreGP.setBackground(Color.WHITE);
        txt_codigoGP.setBackground(Color.WHITE);

        cargarTabla(this.cp.getControladoraCliente().listarProvincias());

    }

