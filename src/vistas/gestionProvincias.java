    private void btn_guardarGPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarGPActionPerformed

        try {
            if (!txt_nombreGP.getText().trim().isEmpty()) {
                String nombre = txt_nombreGP.getText().trim();
                if (txt_codigoGP.getText().trim().isEmpty()) {
                    Provincia provincia = new Provincia(nombre);
                    this.cp.getControladoraCliente().registrarProvincia(provincia);

                    JOptionPane.showMessageDialog(null, "Creado con Exito", "Gestion de Provincias", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    int codigo = Integer.valueOf(txt_codigoGP.getText());
                    Provincia provincia = this.cp.getControladoraCliente().buscarProvinciaPorCodigo(codigo);
                    provincia.setNombre(nombre);

                    this.cp.getControladoraCliente().modificarProvincia(provincia);
                    JOptionPane.showMessageDialog(null, "Editado con Exito", "Gestion de Provincias", JOptionPane.INFORMATION_MESSAGE);

                }
                this.estadoInicial();

            } else {

                throw new Exception("Todos los campos son obligatorios");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Gestion de Provincias", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btn_guardarGPActionPerformed

