    private void btn_editarGPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarGPActionPerformed
        if (tb_listadoProvinciasGP.getSelectedRow() > -1 && tb_listadoProvinciasGP.getSelectedRowCount() == 1) {

            Integer seleccionado = (Integer) tb_listadoProvinciasGP.getValueAt(tb_listadoProvinciasGP.getSelectedRow(), 0);
            Provincia provinciaSeleccionado = this.cp.getControladoraCliente().buscarProvinciaPorCodigo(seleccionado);

            if (provinciaSeleccionado != null) {
                txt_codigoGP.setText(String.valueOf(provinciaSeleccionado.getCodigo()));
                txt_nombreGP.setText(provinciaSeleccionado.getNombre());

                this.cambiarEstado();
            }

            
            tb_listadoProvinciasGP.clearSelection();
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un provincia", "Gestion de Provincias", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btn_editarGPActionPerformed

